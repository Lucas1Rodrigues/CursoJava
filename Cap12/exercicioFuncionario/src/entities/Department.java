package entities;

public class Department {
	private String name;
	
	//constructors
	public Department(String name) {
		this.name = name;
	}
	
	public Department() {
		
	}

	//get and set
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
